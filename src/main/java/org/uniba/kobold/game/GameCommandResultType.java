package org.uniba.kobold.game;

/**
 * The enum Game command result type.
 */
public enum GameCommandResultType {
    /**
     * Move game command result type.
     */
    MOVE,
    /**
     * Description game command result type.
     */
    DESCRIPTION,
    /**
     * Refresh inventory game command result type.
     */
    REFRESH_INVENTORY,
    /**
     * Refresh background game command result type.
     */
    REFRESH_BACKGROUND,
    /**
     * End game command result type.
     */
    END
}

package org.uniba.kobold.game.minigames;

/**
 * The enum Mini game interaction type.
 */
public enum MiniGameInteractionType {
    /**
     * Info mini game interaction type.
     */
    INFO,
    /**
     * Win mini game interaction type.
     */
    WIN,
    /**
     * Lose mini game interaction type.
     */
    LOSE,
    /**
     * Exit mini game interaction type.
     */
    EXIT,
    /**
     * Unlock mini game interaction type.
     */
    UNLOCK,
    /**
     * End game mini game interaction type.
     */
    END_GAME
}
